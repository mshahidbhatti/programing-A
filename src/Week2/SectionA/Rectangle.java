public class Rectangle{

	private double height;
	private double width;


	Rectangle(){
		height=10;
		width=10;
	}

	public void setHeight(double h){
		height=h;
	}

	public double getHeight(){
		return height;
	}

	public void setWidth(double w){
		width=w;
		
	}

	public double getWidth(){
		return width;
	}



}